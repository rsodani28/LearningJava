
public class DataTypes {
	public static void main(String[] args) {
		short myShortNum = 32766; // Small Whole Number
		int myNum = 5; // Integer (whole number)
		long myLongNum = 9223372036854775806L; //Very Big Whole Number
		float myFloatNum = 5.99f; // Floating point number (6-7 decimal digits)
		double myDoubleNum = 19.99d; // Up to 15 decimal digits
		char myLetter = 'D'; // Character
		boolean myBool = true; // Boolean
		String myText = "Hello"; // String
	}
}
